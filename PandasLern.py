import pandas as pd
import numpy as np

# Создание DataFrame с оценками учеников по 100 бальной шкале
data = {
    'Имя': ['Ученик1', 'Ученик2', 'Ученик3', 'Ученик4', 'Ученик5',
            'Ученик6', 'Ученик7', 'Ученик8', 'Ученик9', 'Ученик10'],
    'Математика': [85, 90, 78, 92, 88, 76, 95, 89, 84, 91],
    'Физика': [80, 85, 79, 88, 90, 82, 86, 87, 83, 89],
    'Химия': [78, 82, 88, 91, 85, 80, 89, 92, 84, 87],
    'Литература': [90, 91, 85, 87, 93, 88, 86, 89, 92, 84],
    'История': [88, 84, 90, 85, 87, 89, 91, 90, 86, 83]
}

df = pd.DataFrame(data)

# Вывод первых нескольких строк DataFrame
print("Первые несколько строк:")
print(df.head())

# Вычисление средней оценки по каждому предмету
average_scores = df.mean(numeric_only=True)
print("\nСредние оценки по предметам:")
print(average_scores)

# Вычисление медианной оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианные оценки по предметам:")
print(median_scores)

# Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"\nQ1 для математики: {Q1_math}")
print(f"Q3 для математики: {Q3_math}")
print(f"IQR для математики: {IQR_math}")

# Вычисление стандартного отклонения по каждому предмету
std_deviation = df.std(numeric_only=True)
print("\nСтандартное отклонение по предметам:")
print(std_deviation)